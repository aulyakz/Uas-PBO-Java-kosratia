    private void klikTabel() {
        tabelpenghuni.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = tabelpenghuni.getSelectedRow();
                if (row != -1) {
                    txtnokamar.setText(tabelpenghuni.getValueAt(row, 0).toString());
                    txtstatus.setSelectedItem(tabelpenghuni.getValueAt(row, 1).toString());
                    txtnama.setText(tabelpenghuni.getValueAt(row, 2).toString());
                    txtalamat.setText(tabelpenghuni.getValueAt(row, 3).toString());
                    txtnohp.setText(tabelpenghuni.getValueAt(row, 4).toString());
                    txtdurasi.setText(tabelpenghuni.getValueAt(row, 5).toString());
                }
            }
        });
    }
    
    private void clearField() {
        txtnokamar.setText("");
        txtstatus.setSelectedItem("terisi");
        txtnama.setText("");
        txtalamat.setText("");
        txtnohp.setText("");
        txtdurasi.setText("");
    }
    
    private void loadTable() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No Kamar");
        model.addColumn("Status");
        model.addColumn("Nama");
        model.addColumn("Alamat");
        model.addColumn("No HP");
        model.addColumn("Durasi");

        try {
            Connection conn = koneksidatabase.getKoneksi();
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM penghuni");

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("nokamar"),
                    rs.getString("status"),
                    rs.getString("nama"),
                    rs.getString("alamat"),
                    rs.getInt("nohp"),
                    rs.getInt("durasi")
                });
            }

            tabelpenghuni.setModel(model);

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Gagal load tabel: " + e.getMessage());
        }
    }


