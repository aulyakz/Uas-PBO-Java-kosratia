    private void btntambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btntambahActionPerformed
        try {
            Connection conn = koneksidatabase.getKoneksi();
            String sql = "INSERT INTO penghuni (nokamar, status, nama, alamat, nohp, durasi) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement pst = conn.prepareStatement(sql);

            pst.setInt(1, Integer.parseInt(txtnokamar.getText()));
            pst.setString(2, txtstatus.getSelectedItem().toString());
            pst.setString(3, txtnama.getText());
            pst.setString(4, txtalamat.getText());
            pst.setInt(5, Integer.parseInt(txtnohp.getText()));
            pst.setInt(6, Integer.parseInt(txtdurasi.getText()));

            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil ditambahkan!");

            loadTable();   // refresh tabel
            clearField();  // bersihkan textfield

        } catch (HeadlessException | NumberFormatException | SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menambah data: " + e.getMessage());
        }    
    }//GEN-LAST:event_btntambahActionPerformed

    private void btnhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhapusActionPerformed
        int row = tabelpenghuni.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(null, "Pilih data dulu di tabel!");
            return;
        }

        int konfirmasi = JOptionPane.showConfirmDialog(
                null,
                "Yakin ingin menghapus data ini?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                Connection conn = koneksidatabase.getKoneksi();
                String sql = "DELETE FROM penghuni WHERE nokamar = ?";
                PreparedStatement pst = conn.prepareStatement(sql);

                pst.setInt(1, Integer.parseInt(txtnokamar.getText()));
                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data berhasil dihapus!");

                loadTable();   // refresh tabel
                clearField();  // kosongkan textfield

            } catch (HeadlessException | NumberFormatException | SQLException e) {
                JOptionPane.showMessageDialog(null, "Gagal hapus data: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnhapusActionPerformed
    
